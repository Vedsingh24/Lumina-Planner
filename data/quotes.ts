/**
 * Returns the next unseen quote, cycling through all before repeating.
 * Uses localStorage to track which quotes have been shown.
 */
export function getNextQuote(): { text: string; author: string } {
  const SEEN_KEY = 'lumina_seen_quotes';
  const seenRaw = localStorage.getItem(SEEN_KEY);
  let seen: number[] = seenRaw ? JSON.parse(seenRaw) : [];

  // If all quotes have been seen, reset the cycle
  if (seen.length >= QUOTES.length) {
    seen = [];
  }

  // Find unseen indices
  const unseenIndices = QUOTES.map((_, i) => i).filter(i => !seen.includes(i));

  // Pick a random unseen quote
  const randomIndex = unseenIndices[Math.floor(Math.random() * unseenIndices.length)];

  // Mark as seen
  seen.push(randomIndex);
  localStorage.setItem(SEEN_KEY, JSON.stringify(seen));

  return QUOTES[randomIndex];
}
